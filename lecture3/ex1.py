def div(a):
    if a%2==0:
        print(a, "is even")
    else:
        print(a, "is odd")
    
    if a%3==0:
        print(a, "is divisible by 3")
    else:
        print(a, "is not divisible by 3")
    
    if a%5==0:
        print(a, "is divisible by 5")
    else:
        print(a, "is not divisible by 5")
    

div(40)